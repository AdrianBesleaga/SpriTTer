package com.cgm.spriTTer.domain;

import java.io.Serializable;
import java.util.Collections;

import com.cgm.spriTTer.builder.ArtefactBuilder;

@SuppressWarnings("serial")
public class Message implements Serializable {
	private String text;
	private String user;
	private String date;

	public Message() {
	}

	public Message(String text, String user, String time) {
		super();
		this.text = text;
		this.user = user;
		this.date = time;
		ArtefactBuilder.addMessage(user, this);
		Collections.reverse(ArtefactBuilder.messages.get(user));
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

	public String getUser() {
		return user;
	}

	public void setUser(String user) {
		this.user = user;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((date == null) ? 0 : date.hashCode());
		result = prime * result + ((text == null) ? 0 : text.hashCode());
		result = prime * result + ((user == null) ? 0 : user.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Message other = (Message) obj;
		if (date == null) {
			if (other.date != null)
				return false;
		} else if (!date.equals(other.date))
			return false;
		if (text == null) {
			if (other.text != null)
				return false;
		} else if (!text.equals(other.text))
			return false;
		if (user == null) {
			if (other.user != null)
				return false;
		} else if (!user.equals(other.user))
			return false;
		return true;
	}
	
	

}
